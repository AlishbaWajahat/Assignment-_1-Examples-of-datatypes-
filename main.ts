//Example of primitive data types
// Example of string
var Name:string="Aliishba";
console.log(Name);

// Example of age
var Age:number=23;
console.log(Age);

// Example of boolean
var Interest:boolean=true;
console.log(Interest);

// Example of null
var Assets:null=null;
console.log(Assets);

// Example of undefined
var Qualification:undefined=undefined;
console.log(Qualification);

// Example of symbol
var id:symbol=Symbol("id");
console.log(id.description);

